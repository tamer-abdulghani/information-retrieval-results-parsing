    private void UpdateGui() {
        jComboBox1.setModel(new DefaultComboBoxModel(this.topics.toArray()));
        RefreshUserTopics();
    }

    private ArrayList<UserTopic> getListOfUserTopics(Topic selected) {
        ArrayList<UserTopic> model = new ArrayList<UserTopic>();
        System.out.println("ut11" + this.uF.size());
        for (UserFormulation uf : this.uF) {
            System.out.println("ut11" + uf.getUserId());
            for (UserTopic ut : uf.getTopics()) {
                System.out.println("ut" + ut.getTitle());
                if (ut.getNum() == selected.getNumber()) {
                    model.add(ut);
                }
            }
        }
        return model;
    }

    private void RefreshUserTopics() {
        Topic selected = (Topic) jComboBox1.getSelectedItem();
        System.out.println("" + selected.getDescription());
        if (selected != null) {
            ArrayList<UserTopic> model = getListOfUserTopics(selected);
            jComboBox2.setModel(new DefaultComboBoxModel(model.toArray()));
        }
        //jComboBox2.setModel(new DefaultComboBoxModel(this.basicStocks.toArray()));
    }

    private boolean isDataReady() {

        if (this.topics.size() == 0) {
            JOptionPane.showMessageDialog(null, "Please load the Topics/Queris XML File!!!");
            return false;
        }

        if (this.uF.size() == 0) {
            JOptionPane.showMessageDialog(null, "Please load the User Formulations XML Directory!!!");
            return false;
        }

        if (this.map.size() == 0) {
            JOptionPane.showMessageDialog(null, "Please load the User Queries Formulation Evaluation CSV File!!!");
            return false;
        }

        if (jComboBox1.getSelectedItem() == null || jComboBox2.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "Please select Query and User Formulation First!!!");
            return false;
        }

        return true;
    }
