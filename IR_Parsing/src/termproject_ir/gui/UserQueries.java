    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        JFileChooser c = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Only xml files", "xml", "xml");
        c.setFileFilter(filter);
        int rVal = c.showOpenDialog(this);
        if (rVal == JFileChooser.APPROVE_OPTION) {
            String fullpath = c.getSelectedFile().getPath();
            //jLabel2.setText("File Path:" + fullpath);
            ArrayList<Topic> a = this.controller.ReadParseXml(fullpath);
            for (Topic t : a) {
                this.topics.addElement(t);
            }
            UpdateGui();
        }
        if (rVal == JFileChooser.CANCEL_OPTION) {
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        JFileChooser j = new JFileChooser();
        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        Integer opt = j.showSaveDialog(this);
        if (opt == JFileChooser.APPROVE_OPTION) {
            String fulldirpath = j.getSelectedFile().getPath();
            //jLabel1.setText("File Path:" + fulldirpath);
            this.uF = this.controller.ReadParseXmlDirectory(fulldirpath);
            for (UserFormulation uf : this.uF) {
                this.uFModel.addElement(uf);
            }
            UpdateGui();
        }

    }//GEN-LAST:event_jButton3ActionPerformed

